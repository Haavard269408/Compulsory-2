
/// <summary>
/// adds two integers
/// </summary>
/// <param name="x"></param>
/// <param name="y"></param>
/// <returns>added value</returns>
int add(int x, int y) {
	return x + y;
}

/// <summary>
/// subtract two integers
/// </summary>
/// <param name="x"></param>
/// <param name="y"></param>
/// <returns>subtractec value</returns>
int subtract(int x, int y) {
	return x - y;
}

/// <summary>
/// multiply two integers
/// </summary>
/// <param name="x"></param>
/// <param name="y"></param>
/// <returns>multiplied value</returns>
int multiply(int x, int y) {
	return x * y;
}

/// <summary>
/// divide two integers
/// </summary>
/// <param name="x"></param>
/// <param name="y"></param>
/// <returns>divided value</returns>
int divide(int x, int y) {
	return x / y;
}

